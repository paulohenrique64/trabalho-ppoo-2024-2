package utilitarios;

/**
 * Enum que representa as direções possíveis de movimentação dentro do mapa da simulação.
 * Pode ser utilizada para indicar a direção do deslocamento de veículos ou para
 * controle de navegação.
 * 
 * @author Paulo Henrique Ribeiro Alves
 */
public enum Direcao {
    DIREITA,
    ESQUERDA,
    CIMA,
    BAIXO,
    DIAGONAL_DIREITA_CIMA,
    DIAGONAL_DIREITA_BAIXO,
    DIAGONAL_ESQUERDA_CIMA,
    DIAGONAL_ESQUERDA_BAIXO
}
